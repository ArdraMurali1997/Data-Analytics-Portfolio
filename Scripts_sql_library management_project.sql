#creating table borrowed with usage details:
CREATE TABLE BORROWED(
T_ID INT,
B_ID INT,
M_ID VARCHAR(50),	
BORROW_DATE DATE,
DUE_DATE DATE,	
RETURN_DATE DATE,
FOREIGN KEY (M_ID) REFERENCES MEMBERS(M_ID)
