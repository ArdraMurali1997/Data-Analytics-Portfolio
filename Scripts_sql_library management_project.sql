B_ID INT PRIMARY KEY,
TITLE VARCHAR(30),	
A_ID VARCHAR(20),	
GENRE VARCHAR(15),	
PUBLISHER VARCHAR(50),	
P_DATE DATE,	
PRICE FLOAT,	
