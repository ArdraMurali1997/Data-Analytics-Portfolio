##(Library can stock books or manage the requests befor hand the month/season)
#----------------------------------------------------------------------------------------------------
select monthname(borrow_date) as Month,count(b_id) as No_of_books_borrowed
from borrowed
group by Month;

#5. Members who never borrowed after their last return:
##(Library can identify and contact people to see if there are chances of cancellation of membership)
#----------------------------------------------------------------------------------------------------
select max(br.borrow_date) as last_borrowed,m.m_name,max(br.return_date) as last_returned
from borrowed br join members m
on m.m_id=br.m_id
group by m.m_id,m.m_name
having max(br.borrow_date)<=max(br.return_date);

